public class FinishPoint {
    private double x, y;
    private double radius;

    public FinishPoint(double x, double y, double radius) {
        this.x = x;
        this.y = y;
        this.radius = radius;
    }

    // Getters
    public double getX() { return x; }
    public double getY() { return y; }
    public double getRadius() { return radius; }
}